"""
types in python
"""
# int
print(type(1))
print(type(-0))
print(type(0))
print(type(123141231551231521343242))

# float
print(type(1.0))
print(type(1e4))
print(type(1.0e-4))

# complex
print(type(1+1j))
print(type(1.0+1.0j))

# str
print(type('hello'))
print(type("hello"))
print(type('''hello'''))
print(type("""hello"""))

# bool
print(type(True))
print(type(False))

# None
print(type(None))