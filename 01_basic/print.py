# basic print
print("Hello World from Python")

# basic print using single quotes
print('Hello World from Python')

# multiple prints
print("hello", "world", "from", "python")

# print with separator
print("hello", "world", "from", "python", sep="-")

# print using end
print("Fran", "Torres", end="\n")
print("Another line")

# with double quotes and single quotes
print("Hello 'World' from Python")

# with single quotes and double quotes
print('Hello "World" from Python')

# with backslash and double quotes
print("Hello \"World\" from Python")

# with docstring
print("""Hello World from Python""")